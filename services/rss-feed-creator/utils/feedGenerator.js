/**
 * feedGenerator.js
 * Builds and uploads a W3C-valid RSS 2.0 feed using rewritten articles.
 */

import crypto from "crypto";
import { XMLBuilder } from "fast-xml-parser";
import { r2Put } from "../../shared/utils/r2-client.js";
import { info, error } from "#logger.js";

export async function generateFeed(bucket, rewrittenItems) {
  try {
    if (!Array.isArray(rewrittenItems) || rewrittenItems.length === 0) {
      info("rss-feed-creator.generateFeed.skip", { reason: "no items" });
      return;
    }

    const now = new Date();
    const buildDate = now.toUTCString();

    const channelTitle =
      process.env.RSS_FEED_TITLE?.trim() || "Turing’s Torch: AI Weekly";
    const channelLink = "https://jonathan-harris.online";
    const channelDesc =
      process.env.RSS_FEED_DESCRIPTION?.trim() ||
      "AI insights and rewritten articles generated by Jonathan Harris’s AI Podcast Suite.";

    const validItems = rewrittenItems.filter((x) => x && typeof x === "object");

    const feedObj = {
      rss: {
        "@_version": "2.0",
        channel: {
          title: channelTitle,
          link: channelLink,
          description: channelDesc,
          language: "en-gb",
          lastBuildDate: buildDate,
          pubDate: buildDate,
          generator: "AI Podcast Suite – RSS Feed Creator",
          item: validItems.map((item) => {
            const title = item.shortTitle || item.title || "Untitled Article";
            const link = item.shortUrl || item.link || channelLink;
            const pubDate = new Date(item.pubDate || now).toUTCString();

            // 🔹 Branded GUID like ai-news-4xjz93l7p8hd
            const randomId = parseInt(
              crypto.randomBytes(6).toString("hex"),
              16
            )
              .toString(36)
              .slice(0, 12);
            const guidText = `ai-news-${randomId}`;

            // 🔹 CDATA block with rewritten article + branded link
            const rewrittenText = item.rewritten || item.summary || "";
            const cdataHtml = `
<strong>${escapeHtml(title)}</strong><br/><br/>
${rewrittenText.trim()}<br/><br/>
<a href="${link}">Read on Jonathan-Harris RSS Feed</a>`;

            return {
              title: escapeXml(title),
              link,
              guid: { "@_isPermaLink": "false", "#text": guidText },
              pubDate,
              description: { "#cdata": cdataHtml },
            };
          }),
        },
      },
    };

    const builder = new XMLBuilder({
      format: true,
      ignoreAttributes: false,
      suppressEmptyNode: true,
    });

    const xmlString =
      '<?xml version="1.0" encoding="UTF-8"?>\n' + builder.build(feedObj);
    const jsonString = JSON.stringify(feedObj, null, 2);

    // ✅ Upload to R2 as RSS XML + JSON mirror
    await r2Put(bucket, "feed.xml", xmlString, "application/rss+xml");
    await r2Put(bucket, "feed.json", jsonString, "application/json");

    info("rss-feed-creator.generateFeed.success", {
      bucket,
      items: validItems.length,
      size: xmlString.length,
    });
  } catch (err) {
    error("rss-feed-creator.generateFeed.fail", err, { bucket });
  }
}

// ──────────────────────────────
// Helpers
// ──────────────────────────────

function escapeHtml(str = "") {
  return String(str)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function escapeXml(str = "") {
  return String(str)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
      }
